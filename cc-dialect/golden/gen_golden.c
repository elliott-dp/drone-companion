        p.accel[0] = 0.5f;      p.accel[1] = -9.8125f;  p.accel[2] = 1.25f;
        p.gyro[0]  = 0.0625f;   p.gyro[1]  = -0.125f;   p.gyro[2]  = 0.25f;
