/* ===========================================================================
 * gen_golden.c — golden-vector generator (development plan Phase 1.2)
 *
 * Encodes one instance of EVERY CC_* message (plus HEARTBEAT and TIMESYNC,
 * which the link depends on) with fixed, documented field values, using the
 * mavgen-generated C headers — i.e. the exact encoder family the PX4 side
 * will use — and dumps the raw MAVLink 2 frames to golden_frames.bin.
 *
 * The Rust side (crates/cc-protocol/tests/golden_roundtrip.rs) parses that
 * file, asserts every field, re-encodes, and asserts byte identity. If C and
 * Rust ever disagree on wire layout or CRC_EXTRA, that test fails. This is
 * the CRC_EXTRA drift detector demanded by spec §3.1 / dev plan Phase 1.3.
 *
 * RULES
 *  - Fixed values are the contract: the Rust test mirrors them literally.
 *    Change them only together, in one commit, with the manifest regenerated.
 *  - All floats are exactly representable in binary (x.5, x.25, x.125 ...)
 *    so cross-language equality asserts are exact, never epsilon-based.
 *  - NaN fields use a pinned quiet-NaN bit pattern (0x7FC00000): the C NAN
 *    macro's payload is not pinned by the standard, and the golden bytes
 *    must be identical on every platform.
 *  - Frame order and header sequence numbers are fixed: frame index == seq
 *    (all frames are finalized on MAVLINK_COMM_0, whose tx counter starts
 *    at 0). FC-originated frames use sysid 1 / comp 1 (MAV_COMP_ID_AUTOPILOT1),
 *    CC-originated frames use sysid 1 / comp 191
 *    (MAV_COMP_ID_ONBOARD_COMPUTER), per spec §3.3.
 *  - Two deliberate edge probes at the end:
 *      frame 14: CC_EVENT whose trailing fields are zero -> exercises
 *                MAVLink 2 zero-truncation (payload shrinks on the wire);
 *      frame 15: TIMESYNC with an all-zero payload -> exercises the
 *                "payload is truncated to a minimum of 1 byte" rule.
 *  - No timestamps, hostnames, or anything non-deterministic may enter the
 *    outputs: regenerating from the same XML must be byte-identical (CI
 *    diffs the committed artifacts against a fresh regeneration).
 *
 * Outputs (written into the directory given as argv[1], default "."):
 *   golden_frames.bin    concatenated raw MAVLink 2 frames
 *   golden_manifest.txt  one line per frame:
 *                        idx offset frame_len msgid hdr_seq sysid compid
 *                        payload_len crc_extra name
 * ======================================================================== */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdint.h>

#include <cc_dialect/mavlink.h>
#include "dialect_hash.h"

#define GOLDEN_SYSID   1
#define COMPID_FC      MAV_COMP_ID_AUTOPILOT1       /* 1   */
#define COMPID_CC      MAV_COMP_ID_ONBOARD_COMPUTER /* 191 */

static FILE    *g_bin;
static FILE    *g_manifest;
static uint32_t g_offset;
static int      g_index;

/* Pinned quiet NaN — see RULES above. */
static float qnan_f32(void)
{
    const uint32_t bits = 0x7FC00000u;
    float f;
    memcpy(&f, &bits, sizeof f);
    return f;
}

static void emit(const mavlink_message_t *msg, const char *name)
{
    uint8_t buf[MAVLINK_MAX_PACKET_LEN];
    const uint16_t len = mavlink_msg_to_send_buffer(buf, msg);
    const mavlink_msg_entry_t *entry = mavlink_get_msg_entry(msg->msgid);

    if (entry == NULL) {
        fprintf(stderr, "gen_golden: no msg entry for id %u (%s)\n",
                (unsigned)msg->msgid, name);
        exit(1);
    }
    if (fwrite(buf, 1, len, g_bin) != len) {
        perror("gen_golden: fwrite");
        exit(1);
    }
    fprintf(g_manifest, "%d %u %u %u %u %u %u %u %u %s\n",
            g_index, g_offset, (unsigned)len, (unsigned)msg->msgid,
            (unsigned)msg->seq, (unsigned)msg->sysid, (unsigned)msg->compid,
            (unsigned)msg->len, (unsigned)entry->crc_extra, name);

    g_offset += len;
    g_index  += 1;
}

